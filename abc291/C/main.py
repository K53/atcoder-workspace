#!/usr/bin/env python3
import sys

YES = "Yes"  # type: str
NO = "No"  # type: str


def solve(N: int, S: str):
    from collections import defaultdict
    x, y = 0, 0
    d = defaultdict(int)
    d[(x, y)] = 1
    for ss in S:
        if ss == "U":
            y += 1
            d[(x, y)] += 1
        elif ss == "D":
            y -= 1
            d[(x, y)] += 1
        elif ss == "R":
            x += 1
            d[(x, y)] += 1
        elif ss == "L":
            x -= 1
            d[(x, y)] += 1
    for dd in d.values():
        if dd >= 2:
            print(YES)
            return
    print(NO)
    return


