import sys
from collections import deque
INF = 10 ** 16

def solve(N: int, M: int, u: "List[int]", v: "List[int]"):
    G = [set() for _ in range(N)]
    for uu, vv in zip(u, v):
        G[uu - 1].add(vv - 1)
    
    def bfs(start_node: int) -> list:
        q = deque()
        dist = [INF] * N
        q.append(start_node)
        dist[start_node] = 0
        while q:
            now = q.popleft()
            for next in G[now]:
                if dist[next] != INF:
                    continue
                q.append(next)
                dist[next] = dist[now] + 1
        return dist

    ans = 0
    for st in range(N):
        d = bfs(st)
        for dest in range(N):
            if st == dest or d[dest] == INF or dest in G[st]:
                continue
            else:
                ans += 1
    print(ans)
    return


