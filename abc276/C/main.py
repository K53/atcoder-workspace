#!/usr/bin/env python3
import sys

class PermutationFactory():
    def __init__(self, N: int) -> None:
        """
        階乗算出
        O(N)
        """
        self.N = N
        self.fac = [1, 1] # 階乗リスト
        for i in range(2, N + 1):
            self.fac.append(self.fac[i - 1] * i)
        return
    
    def kth_permutation(self, k: int) -> list:
        """
        k番目の順列を返す。O(N)
        先頭要素から固定していく。その要素を固定したときの順列の場合の和をkから引いていく。
        args:
            k: 1〜Nの順列の昇順k番目 (0-index)
        return:
            res: k番目のリスト
        """
        candidates = list(range(self.N)) # 任意の配列でもいいが重複がないこと
        res = []
        for i in reversed(range(self.N)):
            a = self.fac[i]
            # print(a)
            j, k = divmod(k, a)
            res.append(candidates[j])
            del candidates[j]
        return res

    def get_kth_of_permutation(self, L: list) -> int:
        """
        args:
            L: 1〜Nまでの順列で昇順何番目かを求めたい順列
        return:
            k: 昇順何番目か (0-index)
        """
        k = 0
        while len(L) > 1:
            a = len([ll for ll in L if ll < L[0]])
            k += a * self.fac[len(L) - 1]
            L = L[1:]
        return k


def solve(N: int, P: "List[int]"):
    P = [i - 1 for i in P]
    pf = PermutationFactory(N)
    k = pf.get_kth_of_permutation(P)
    print(*[a + 1 for a in pf.kth_permutation(k - 1)])
    return


