def solve(N: int, M: int, A: "List[int]", B: "List[int]"):
    from collections import deque
    def bfs(G: "List[to]", start_node: int) -> list:
        INF = 10 ** 16
        q = deque()
        dist = [INF] * len(G)
        q.append(start_node)
        dist[start_node] = 0
        while q:
            now = q.popleft()
            for next in G[now]:
                if dist[next] != INF:
                    continue
                q.append(next)
                dist[next] = dist[now] + 1
        return dist
    G = [[] for _ in range(N)]
    for i in range(M):
        G[A[i] - 1].append(B[i] - 1)
        G[B[i] - 1].append(A[i] - 1)
    for i in range(N):
        d = bfs(G, i)
        print(d.count(2))
    return


