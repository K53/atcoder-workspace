#!/usr/bin/env python3
import sys

def main():
    X, K, D =map(int, input().split())
    X = abs(X)
    p, q = divmod(X, D)
    if p >= K:
        print(X - K * D)
        return
    K -= p
    if K % 2 == 0:
        print(q)
    else:
        print(D - q)
    return




if __name__ == '__main__':
    main()
