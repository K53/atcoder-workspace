def main():
    def iterate_tokens():
        for line in sys.stdin:
            for word in line.split():
                yield word
    tokens = iterate_tokens()
    X = next(tokens)  # type: str
    M = int(next(tokens))  # type: int
    solve(X, M)

if __name__ == '__main__':
    main()
