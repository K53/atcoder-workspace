#!/usr/bin/env python3
import sys


def solve(N: str):
    hand = -1 # L
    pre = "-1"
    ans = 0
    for s in N:
        newHand = 0 if s in "12345" else 1
        if pre == "-1":
            ans += 500
            hand = newHand
            pre = s
            continue
        if s == pre:
            ans += 301
            hand = newHand
            pre = s
            continue
        if hand == newHand:
            ans += 210
            hand = newHand
            pre = s
            continue
        ans += 100
        hand = newHand
        pre = s
    print(ans)
    return


