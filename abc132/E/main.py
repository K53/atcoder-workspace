import sys
from collections import deque
INF = 10 ** 16

def bfs(G: "List[to]", start_node: int) -> list:
    q = deque()
    dist = [[INF] * 3 for _ in range(len(G))]
    dist[start_node][0] = 0
    q.append((start_node, 0))
    while q:
        now, now_step = q.popleft()
        for next in G[now]:
            if dist[next][(now_step + 1) % 3] != INF:
                continue
            q.append((next, (now_step + 1) % 3))
            dist[next][(now_step + 1) % 3] = dist[now][now_step] + 1
    return dist


def solve(N: int, M: int, u: "List[int]", v: "List[int]", S: int, T: int):
    G = [[] for _ in range(N)]
    for uu, vv in zip(u, v):
        G[uu - 1].append(vv - 1)
    d = bfs(G, S - 1)
    if d[T - 1][0] == INF:
        print(-1)
    else:
        print(d[T - 1][0] // 3)
    return


