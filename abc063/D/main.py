#!/usr/bin/env python3
import sys

def solve(N: int, A: int, B: int, h: "List[int]"):
    # True ------ ok | ng ---- False
    def is_ok(k: int):
        count = 0
        for hh in h:
            hh -= k * B
            if hh < 0:
                continue
            p, q = divmod(hh, (A - B))
            count += p + (1 if q > 0 else 0)
        # print(count)
        return count <= k    # 条件式

    def binSearch(ok: int, ng: int):
        # print(ok, ng)              # はじめの2値の状態
        while abs(ok - ng) > 1:     # 終了条件（差が1となり境界を見つけた時)
            mid = (ok + ng) // 2
            # print("target > ", mid)
            result = is_ok(mid)
            # print(result)
            if result:
                ok = mid            # midが条件を満たすならmidまではokなのでokの方を真ん中まで持っていく
            else:
                ng = mid            # midが条件を満たさないならmidまではngなのでngの方を真ん中まで持っていく
            # print(ok, ng)          # 半分に切り分ける毎の2値の状態
        return ok   
    print(binSearch(10 ** 14 + 1, -1))
    return


