#!/usr/bin/env python3
import sys

YES = "Yes"  # type: str
NO = "No"  # type: str


def solve(S: str):
    st = -1
    for i in range(len(S)):
        if S[i] == "o":
            st = i
            break
    if st >= 3:
        print(NO)
        return

    if st == -1:
        if len(S) >= 3:
            print(NO)
            return
        else:
            print(YES)
            return

    for i in range(st, len(S)):
        if (i - st) % 3 == 0:
            if S[i] == "o":
                continue
            else:
                print(NO)
                return
        else:
            if S[i] == "o":
                print(NO)
                return
            else:
                continue
    print(YES)
    return


