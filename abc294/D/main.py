def main():
    N, Q = map(int, input().split())
    hd = HeapDict()
    next_call = 1
    for _ in range(Q):
        l = list(map(int, input().split()))
        if l[0] == 1:
            hd.insert(next_call)
            next_call += 1
        elif l[0] == 2:
            hd.erase(l[1])
        else:
            print(hd.dryPop())
    return    

if __name__ == '__main__':
    main()
