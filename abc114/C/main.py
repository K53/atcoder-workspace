#!/usr/bin/env python3
import sys


def solve(N: int):
    def dfs(next: int):
        res = 0
        nums = [3, 5, 7]
        # --- 探索終了条件 ----------------------------
        if next > N:
            return res
        # --- カウントアップ条件 -----------------------
        S = str(next)
        check = True
        for i in nums:
            if not str(i) in S:
                check = False
        if check:
            res += 1
        # --- 次の探索(分岐) --------------------------
        for num in nums:
            res += dfs(next * 10 + num)
        return res
    a = dfs(0)
    print(a)
    return


