#!/usr/bin/env python3
import sys
from itertools import groupby

def runLengthEncode(S: str) -> "List[tuple(str, int)]":
    grouped = groupby(S)
    res = []
    for k, v in grouped:
        res.append((k, int(len(list(v)))))
    return res

def main():
    N = int(input())
    S = input()
    print(len(runLengthEncode(S)))
    return

if __name__ == '__main__':
    main()
