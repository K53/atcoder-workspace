#!/usr/bin/env python3
import sys

def solve(N: int, X: int, Y: int):
    from collections import deque
    def bfs(edges: "List[to]", start_node: int) -> list:
        INF = 10 ** 16
        q = deque()
        dist = [INF] * len(edges)
        q.append(start_node)
        dist[start_node] = 0
        while q:
            now = q.popleft()
            for next in edges[now]:
                if dist[next] != INF:
                    continue
                q.append(next)
                dist[next] = dist[now] + 1
        return dist
    G = [[] for _ in range(N)]
    for i in range(N - 1):
        G[i].append(i + 1)
        G[i + 1].append(i)
    G[X - 1].append(Y - 1)
    G[Y - 1].append(X - 1)
    ans = [0] * N
    for i in range(N):
        d = bfs(G, i)
        for dd in d:
            ans[dd] += 1
    for i in range(1, N):
        print(ans[i] // 2)

    return


