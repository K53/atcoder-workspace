NO = "Impossible"  # type: str
def solve(N: int, S: int, A: "List[int]", B: "List[int]"):
    INF = 10 ** 12
    dp = [[INF] * (S + 1) for _ in range(N + 1)]
    dp[0][0] = 0
    for i in range(N):
        for j in range(S + 1):
            if dp[i][j] != INF:
                if j + A[i] < S + 1:
                    dp[i + 1][j + A[i]] = 1
                if j + B[i] < S + 1:
                    dp[i + 1][j + B[i]] = 1
    if dp[-1][-1] == INF:
        print(NO)
        return
    
    ss = S
    ans = []
    for i in reversed(range(1, N + 1)):
        if ss - A[i - 1] >= 0 and dp[i - 1][ss - A[i - 1]] != INF:
            ans.append("A")
            ss -= A[i - 1]
            continue
        if ss - B[i - 1] >= 0 and dp[i - 1][ss - B[i - 1]] != INF:
            ans.append("B")
            ss -= B[i - 1]
            continue
    print(*ans[::-1], sep="")
    return


