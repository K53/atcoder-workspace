#!/usr/bin/env python3
import sys

def solve(N: int):
    if N % 2 == 1:
        print()
        return
    for n in range(2 ** N):
        count = 0
        ans = []
        for i in range(N):
            if (n >> i) & 1 == 0:
                ans.append("(")
            else:
                ans.append(")")
        for s in ans[::-1]:
            if s == "(":
                count += 1
            else:
                count -= 1
                if count < 0:
                    break
        else:
            if count == 0:
                print(*ans[::-1], sep="")
    return


