#!/usr/bin/env python3
from collections import defaultdict
import sys

YES = "Yes"  # type: str
NO = "No"  # type: str




def main():
    N = int(input())
    names = []
    d = defaultdict(int)
    for _ in range(N):
        n = list(input().split())
        names.append(n)
        d[n[0]] += 1
        d[n[-1]] += 1
    # print(d)
    for n in names:
        if n[0] == n[1]:
            if d[n[0]] > 2:
                print(NO)
                return  
        elif d[n[0]] > 1 and d[n[-1]] > 1:
            print(NO)
            return
    print(YES)
    return


if __name__ == '__main__':
    main()
