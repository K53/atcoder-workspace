#!/usr/bin/env python3
import sys
import math

def solve(A: int, B: int):
    
    # 最小にしたい関数f
    def f(k: int) -> float:
        return A / pow(k + 1, 1/2) + B * k
    
    def ternarySearch(l: float, r: float, accept_range: int = 2) -> tuple:
        """
        - l : 定義域(l ≦ x ≦ r)の左端
        - r : 定義域(l ≦ x ≦ r)の右端
        - accept_range : 探索で絞った後の範囲
        三分探索を行い、最小値を取る値の範囲l ~ rを返却する。
        """
        # 範囲が accept_range 以下に絞られるまで回す。
        while r - l > accept_range:
            # オーバーフローしないための三等分点を置く
            mid1 = l + (r - l) // 3
            mid2 = r - (r - l) // 3
            if f(mid1) < f(mid2):
                r = mid2
            else:
                l = mid1
        return (l, r)
    
    l, r = ternarySearch(l=0, r=10 ** 18)
    ans = 10 ** 18
    for i in range(math.floor(l), math.ceil(r)):
        ans = min(ans, f(i))
    print(ans)
    return

