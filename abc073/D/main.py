#!/usr/bin/env python3
import sys
INF = 10 ** 16
class WarshallFloyd():
    def __init__(self, N):
        self.N = N
        dp = [[INF] * N for _ in range(N)]
        for i in range(N):
            dp[i][i] = 0
        self.dp = dp
    
    # 自己ループを持つグラフの扱いは注意。
    def addEdge(self, fromNode: int, toNode: int, cost: int = 1):
        self.dp[fromNode][toNode] = cost
    
    def build(self):
        """
        やっていることとしては、
        0 〜 (via - 1)までの地点だけを利用して求めたdpテーブルを使い、viaを経由地とした時の更新処理している。
        """
        for via in range(self.N):
            for start in range(self.N):
                for goal in range(self.N):
                    self.dp[start][goal] = min(self.dp[start][goal], self.dp[start][via] + self.dp[via][goal])
        return self.dp

def solve(N: int, M: int, R: int, r: "List[int]", A: "List[int]", B: "List[int]", C: "List[int]"):
    wf = WarshallFloyd(N)
    for i in range(M):
        wf.addEdge(A[i] - 1, B[i] - 1, C[i])
        wf.addEdge(B[i] - 1, A[i] - 1, C[i])
    d = wf.build()
    import itertools
    ans = INF
    for l in itertools.permutations([i - 1 for i in r]):
        dist = 0
        for k in range(R - 1):
            dist += d[l[k]][l[k + 1]]
        ans = min(ans, dist)
    print(ans)
    return


